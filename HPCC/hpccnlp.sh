#! /bin/bash
echo " "
echo "========================================================================";
echo "=  Download deb file";
echo "========================================================================";
wget http://hpccdeb.visualtext.org -O hpccnlp.deb;
echo " "
echo "========================================================================";
echo "= Install HPCC & NLP Plugin package";
echo "========================================================================";
dpkg -i hpccnlp.deb;
echo " "
echo "========================================================================";
echo "= Apt Update";
echo "========================================================================";
apt update;
echo " "
echo "========================================================================";
echo "= Apt Install";
echo "========================================================================";
apt install -f -y;
echo " "
echo "========================================================================";
echo "= Start HPCC server";
echo "========================================================================";
echo " ";
/etc/init.d/hpcc-init start;
